# --- PROFESSIONAL PDF ENGINE ---
def generate_pdf(type, data):
    filename = f"{type}_{data['phone'][-4:]}_{random.randint(100,999)}.pdf"
    filepath = os.path.join(PDF_FOLDER, filename)
    c = canvas.Canvas(filepath, pagesize=letter)
    
    # Visuals
    c.setFillColor(colors.darkblue)
    c.rect(0, 700, 612, 100, fill=1, stroke=0)
    c.setFillColor(colors.white)
    c.setFont("Helvetica-Bold", 20)
    c.drawCentredString(300, 750, "YOJNA SEVA KENDRA")
    c.setFont("Helvetica", 12)
    c.drawCentredString(300, 730, "Official Service Receipt")
    
    c.setFillColor(colors.black)
    c.setFont("Helvetica", 12)
    y = 650
    for k, v in data.items():
        c.drawString(50, y, f"{k}: {v}")
        y -= 25
    
    c.rect(50, y-20, 500, 2)
    c.setFont("Helvetica-Oblique", 10)
    c.drawString(50, y-40, "Generated by Yojna-GPT AI Agent.")
    
    c.save()
    return filename

# --- INTELLIGENT OFFLINE BRAIN (Zero-Lag) ---
def smart_offline_ai(query):
    q = query.lower()
    # 1. Chat
    if any(x in q for x in ["hi", "hello", "hey", "start"]):
        return "üáÆüá≥ *Namaste!* I am Yojna-GPT. I can help you find loans and schemes.\n\n*Try asking:* 'Loan for factory' or 'Farming subsidy'."
    if any(x in q for x in ["who are you", "bot", "ai"]):
        return "ü§ñ I am an AI Agent designed to help Indians find government benefits."
    if any(x in q for x in ["bye", "thanks", "good"]):
        return "üôè You're welcome! Type 'Hi' anytime you need help."
        
    # 2. Scheme Intent
    if "loan" in q or "money" in q or "fund" in q:
        return "ü§ñ *AI Recommendation:* For business loans, apply for **PMEGP** (ID 101) or **Mudra** (ID 102). For farming, check **KCC** (ID 202)."
    if "farm" in q or "agri" in q:
        return "ü§ñ *AI Recommendation:* Farmers should check **PM Kisan** (ID 201) or **National Livestock** (ID 203)."
    if "student" in q or "study" in q:
        return "ü§ñ *AI Recommendation:* Students can check **Vidya Lakshmi Loans** (ID 501)."
    if "solar" in q or "power" in q:
        return "ü§ñ *AI Recommendation:* Apply for **PM Surya Ghar** (ID 601) for free electricity."
    
    return "ü§ñ *AI:* I found relevant schemes. Try searching for specific categories like 'Business', 'Health', or 'Education'."

# --- MULTI-MODEL AI ENGINE (Self-Healing) ---
def get_ai_reply(query):
    if not API_KEY: return smart_offline_ai(query)

    # List of models to try in order of preference
    models = ['gemini-1.5-flash', 'gemini-1.5-pro', 'gemini-pro']
    
    genai.configure(api_key=API_KEY)
    
    for m in models:
        try:
            model = genai.GenerativeModel(m)
            res = model.generate_content(f"Act as a govt scheme expert. Answer briefly: {query}")
            return f"ü§ñ *AI Assistant:*\n{res.text}"
        except Exception as e:
            print(f"Model {m} failed: {e}")
            continue # Try next model
            
    # If all fail, use offline brain
    return smart_offline_ai(query)

